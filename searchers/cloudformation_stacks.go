package searchers

import (
	"context"
	"fmt"
	"strings"

	"github.com/aws/aws-sdk-go-v2/aws"
	"github.com/aws/aws-sdk-go-v2/service/cloudformation"
	"github.com/aws/aws-sdk-go-v2/service/cloudformation/types"
	aw "github.com/deanishe/awgo"
	"github.com/rkoval/alfred-aws-console-services-workflow/awsworkflow"
	"github.com/rkoval/alfred-aws-console-services-workflow/caching"
	"github.com/rkoval/alfred-aws-console-services-workflow/util"
)

type CloudFormationStackSearcher struct{}

func (s CloudFormationStackSearcher) Search(wf *aw.Workflow, query string, cfg aws.Config, forceFetch bool, fullQuery string) error {
	cacheName := util.GetCurrentFilename()
	entities := caching.LoadCloudformationStackArrayFromCache(wf, cfg, cacheName, s.fetch, forceFetch, fullQuery)
	for _, entity := range entities {
		s.addToWorkflow(wf, query, cfg, entity)
	}
	return nil
}

func (s CloudFormationStackSearcher) fetch(cfg aws.Config) ([]types.Stack, error) {
	svc := cloudformation.NewFromConfig(cfg)

	pageToken := ""
	var entities []types.Stack
	for {
		params := &cloudformation.DescribeStacksInput{}
		if pageToken != "" {
			params.NextToken = aws.String(pageToken)
		}
		resp, err := svc.DescribeStacks(context.TODO(), params)
		if err != nil {
			return nil, err
		}

		entities = append(entities, resp.Stacks...)

		if resp.NextToken != nil {
			pageToken = *resp.NextToken
		} else {
			break
		}
	}

	return entities, nil
}

func (s CloudFormationStackSearcher) addToWorkflow(wf *aw.Workflow, query string, config aws.Config, entity types.Stack) {
	title := *entity.StackName
	tagName := util.GetCloudFormationTagValue(entity.Tags, "Name")
	// if stack was generated by ElasticBeanstalk and has the generated name, append name tag
	if strings.HasPrefix(title, "awseb-") && tagName != "" {
		title += fmt.Sprintf(" (%s)", tagName)
	}
	subtitle := *entity.Description

	item := util.NewURLItem(wf, title).
		Subtitle(subtitle).
		Arg(fmt.Sprintf("https://%s.console.aws.amazon.com/cloudformation/home?region=%s#/stacks/stackinfo?stackId=%s", config.Region, config.Region, *entity.StackId)).
		Icon(awsworkflow.GetImageIcon("cloudwatch"))

	// if stack was generated by ElasticBeanstalk and has the generated name, use the name tag instead
	if strings.HasPrefix(title, "awseb-") && !strings.HasPrefix(query, "awseb-") && tagName != "" {
		item.Match(tagName)
	} else {
		item.Match(title)
	}
}
